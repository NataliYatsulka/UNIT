#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int	negative;
	int	nbr;

	i = 0;
	negative = 1;
	nbr = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == 45)
		negative = -1;
	if (str[i] == 45 || str[i] == 43)
		i++;
	while (str[i] >= 48 && str[i] <= 57)
	{
		nbr *= 10;
		nbr += ((int)str[i] - 48);
		i++;
	}
	return (nbr * negative);
}

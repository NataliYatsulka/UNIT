#include "lib.h"

int		ft_check_3(char *s)
{
	int		i;
	int		j;

	i = -1;
	j = 0;
	while (s[++i])
	{
		if (s[i] == '.' || s[i] == '#' || s[i] == '\n')
			j++;
		else
			ft_error();
	}
	if (++j % 21 != 0)
		ft_error();
	i = j++ / 21;
	return (i);
}

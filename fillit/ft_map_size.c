#include "lib.h"

int		ft_map_size(t_list *list)
{
	int		i;
	int		n;

	i = 0;
	n = 2;
	while (list->next)
	{
		list = list->next;
		i++;
	}
	while (n * n < i * 4)
		n++;
	return (n);
}

#include "lib.h"

int		ft_y(char **map, int map_size, int x, int y)
{
	while (x < map_size && map[x][y] != '.')
	{
		while (y < map_size && map[x][y] != '.')
			y++;
		if (map[x][y] == '.')
			break ;
		y = 0;
		x++;
	}
	return (y);
}

#include "libft.h"

char	*ft_put_nb_itoa(int len, int n)
{
	if (n >= 0)
	{
		p[len - i - 1] = '0' + n % 10;
		n /= 10;
		len--;
	}
	else
	{
		p[len - i - 1] = '0' - n % 10;
		n /= 10;
		len--;
	}
	return (p);
}
